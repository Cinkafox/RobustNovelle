using System.Collections.Generic;
using System.Linq;
using Cinka.Game.Dialog.Data;
using Cinka.Game.Dialog.DialogActions;
using Robust.Client.AutoGenerated;
using Robust.Client.Graphics;
using Robust.Client.ResourceManagement;
